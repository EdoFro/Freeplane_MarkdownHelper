      ## 5. Watterson
    </p>
    <p>
      
    </p>
    <p>
      -----
    </p>
    <p>
      
    </p>
    <p>
      William Boyd &quot;Bill&quot; Watterson II (born July 5, 1958) is the author of [Calvin and Hobbes](https://calvinandhobbes.fandom.com/wiki/Calvin_and_Hobbes). He was author and artist during the strip's decade-long run. Calvin and Hobbes abruptly ceased publication in 1995, when Watterson decided to retire. He is now removed completely from the public eye, and is reluctant to take interviews, preferring to let his work speak for itself. He drew Calvin's father to look exactly like himself, for symbolism. Bill is currently 62 years old.
    </p>
