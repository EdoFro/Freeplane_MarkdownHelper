  <body>
    <p>
      In $1, story arcs are a theme or plotline that lasts longer than a day. They can range from a few days to a month, such as the arc with $2 accidentally pushing $3 into a ditch.
    </p>
    <p>
      
    </p>
    <p>
      Generally, the strip had week-long story arcs for the first couple of years and had its longest arcs in the middle of the its life. Later on, the arcs became fewer and farther between as $4 focused more on $5.
    </p>
  </body>
