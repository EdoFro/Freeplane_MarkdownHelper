      Despite $1's reluctance to sell $2 based on $2 or its characters, he still allowed some merchandise to reach the public, one of the few was books. There have so far been eighteen books (including one three-volume set) published in the United States.
    </p>
    <p>
      
    </p>
    <p>
      The books, referred to as &quot;Collections&quot;, form a complete archive of the newspaper strips, except for a $4 from November 28, 1985 (The collections do contain a strip for this date, but it is not the same strip that appeared in some newspapers). &quot;Treasuries&quot; combine the two preceding collections with bonus material and include color reprints of Sunday comics, and irregular printed collections reprint already published comics with a particular intent, such as providing original sketches or commentary.
    </p>
    <p>
      
    </p>
    <p>
      A complete collection of Calvin and Hobbes strips, in three hardcover volumes, with a total 1440 pages, was released on October 4, 2005, by $5 Publishing. It also includes color prints of the art used on paperback covers, the Treasuries' extra illustrated stories and poems, and an original 14-page introduction by Bill Watterson. Unfortunately, the alternate 1985 strip is still omitted, and two other strips (January 7, 1987, and November 25, 1988) have altered dialogue.
    </p>
    <p>
      
    </p>
    <p>
      To celebrate the release, Calvin and Hobbes reruns were made available to newspapers from Sunday, September 4, 2005, through Saturday, December 31, 2005, and Bill Watterson answered a select dozen questions submitted by readers. ($6 $7) Like current contemporary strips, weekday Calvin and Hobbes strips now appear in color print when available, instead of black and white as in their first run.
